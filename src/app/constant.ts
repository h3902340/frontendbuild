export function idCivToPathName(idCiv: number): string {
    if (idCiv == 0) return `${flagRoot}${civIdToInfo[49].name}`;
    if (!civIdToInfo[idCiv]) {
        console.error(`unkown civ id: ${idCiv}`);
        return `${flagRoot}${civIdToInfo[49].name}`;
    }
    return `${flagRoot}${civIdToInfo[idCiv].name}`;
}
