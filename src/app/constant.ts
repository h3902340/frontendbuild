const avatarRoot: string = "assets/Data/wpfg/resources/art/profiles/";

export function avatarIdToUrl(ai: number, ac: number): string {
    switch (ac) {
        case 0:
            return `${avatarRoot}avatar_tier0_Starter_${("0" + ai).slice(-2)}.png`;
        case 1:
            return `${avatarRoot}avatar_event0_${("0" + ai).slice(-2)}.png`;
        case 2:
            return `${avatarRoot}avatar_persistent1_${("0" + ai).slice(-2)}.png`;
        case 3:
            if (ai < 100) {
                return `${avatarRoot}avatar_weekly_${("0" + ai).slice(-2)}.png`;
            } else {
                return `${avatarRoot}avatar_weekly_${ai}.png`;
            }
        case 4:
            return `${avatarRoot}avatar_usa_${("0" + ai).slice(-2)}.png`;
        case 5:
            return `${avatarRoot}avatar_mexico_${("0" + ai).slice(-2)}.png`;
        case 6:
            return `${avatarRoot}avatar_italy_malta_${("0" + ai).slice(-2)}.png`;
        default:
            return '';
    }
}

